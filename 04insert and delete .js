let list = [2,3,4]

list.unshift(1)//unshift is use for the add in 0 index
console.log(list);
list.shift(1)//shift is use for the delete into 0 index
console.log(list);

list.push(5);// it is use for the add in last 
console.log(list);
list.pop(5);//it is use for the remove into back side 
console.log(list);

